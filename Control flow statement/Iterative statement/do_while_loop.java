class do_while_loop{
    public static void main(String[] args){
    // do while statement will run loop by default one time and then it check the while condition
      int num = 2;
    do{
      num++;
      System.out.println(num);
    }while(num>=5);
      
   // Empty do while loop
      do;
      while(false);
    
    }
}
